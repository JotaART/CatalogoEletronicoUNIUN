    private void btnTrocaImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrocaImgActionPerformed
        JFileChooser fc = new JFileChooser();
        int res = fc.showOpenDialog(null);

        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();
            try {
                imagem = ManipularImagem.setImagemDimensao(arquivo.getAbsolutePath(), 1280, 720);

                lblImagem.setIcon(new ImageIcon(imagem));
                lblImagem.setText("");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao obter imagem! \n" + ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma imagem");
        }
    }//GEN-LAST:event_btnTrocaImgActionPerformed

    private void btnEditPConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPConfirmActionPerformed
        int selectedRow = jTabelaProdutos.getSelectedRow();
        try {
            Produtosbean produto;
            produto = new Produtosbean();
            produtoDAO pdao = new produtoDAO();

            produto.setImagem(ManipularImagem.getImgBytes(imagem));
            produto.setCodigoref(txtEditPCodRef.getText());
            produto.setCodigo(Integer.parseInt(txtEditPCodigo.getText()));
            produto.setMontadora(txtEditPMontadora.getText());
            produto.setTipopeca(txtEditPTipoPeca.getText());
            produto.setAplicacao(txtEditPAplic.getText());
            produto.setPreco(Double.parseDouble(txtEditPPreco.getText()));

            pdao.update(produto, jTabelaProdutos.getValueAt(selectedRow, 0).toString());
            produtoTableModel.setDados(pDAO.read(""));
            System.out.println(produto);
            
            JOptionPane.showMessageDialog(rootPane, "Produto alterado com sucesso");
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar o produto \n" + ex);
        }
    }//GEN-LAST:event_btnEditPConfirmActionPerformed

