    private void btnAddAConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAConfirmActionPerformed
        Aplicaçãobean aplicacao;
        aplicacao = new Aplicaçãobean();
        aplicaçãoDAO adao = new aplicaçãoDAO();

        if (txtAddACodigo.getText().equals("")
                || txtAddAMontadora.getText().equals("")
                || txtAddAAplicacao.getText().equals("")
                || txtAddAMotor.getText().equals("")
                || txtAddAPistao.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Complete todos os campos para cadastrar");
        } else {
            aplicacao.setCodigo(Integer.parseInt(txtAddACodigo.getText()));
            aplicacao.setMontadora(txtAddAMontadora.getText());
            aplicacao.setModelo(txtAddAAplicacao.getText());
            aplicacao.setMotor(txtAddAMotor.getText());
            aplicacao.setPistao(Double.parseDouble(txtAddAPistao.getText()));

            adao.create(aplicacao);
            JOptionPane.showMessageDialog(rootPane, "Aplicação cadastrada com sucesso!");
            aplicacaoTableModel.setDados(aDAO.read());
        }
    }//GEN-LAST:event_btnAddAConfirmActionPerformed

